// eslint-disable-next-line @typescript-eslint/no-explicit-any
const GestureController = ({ onGesture, onMove, onStatus, debugMode, onPinch, enabled }: any) => {
  const videoRef = useRef<HTMLVideoElement>(null);
  const canvasRef = useRef<HTMLCanvasElement>(null);
  const pinchCooldownRef = useRef<number>(0);

  useEffect(() => {
    if (!enabled) {
      // Cleanup khi disabled
      if (videoRef.current && videoRef.current.srcObject) {
        const stream = videoRef.current.srcObject as MediaStream;
        stream.getTracks().forEach(track => track.stop());
        videoRef.current.srcObject = null;
      }
      return;
    }

    let gestureRecognizer: GestureRecognizer;
    let requestRef: number;
    let isSetup = false;

    const setup = async () => {
      if (isSetup) return; // Ngăn setup lặp lại
      isSetup = true;

      onStatus("ĐANG TẢI AI...");
      try {
        console.log("Bắt đầu tải MediaPipe...");
        const vision = await FilesetResolver.forVisionTasks("https://cdn.jsdelivr.net/npm/@mediapipe/tasks-vision@0.10.3/wasm");
        console.log("MediaPipe tải thành công");
        
        console.log("Bắt đầu tạo GestureRecognizer...");
        gestureRecognizer = await GestureRecognizer.createFromOptions(vision, {
          baseOptions: {
            modelAssetPath: "https://storage.googleapis.com/mediapipe-models/gesture_recognizer/gesture_recognizer/float16/1/gesture_recognizer.task",
            delegate: "GPU"
          },
          runningMode: "VIDEO",
          numHands: 1
        });
        console.log("GestureRecognizer tạo thành công");
        
        onStatus("ĐANG YÊU CẦU CAMERA...");
        if (navigator.mediaDevices && navigator.mediaDevices.getUserMedia) {
          const stream = await navigator.mediaDevices.getUserMedia({ video: { width: 640, height: 480 } });
          if (videoRef.current) {
            videoRef.current.srcObject = stream;
            videoRef.current.onloadedmetadata = () => {
              videoRef.current?.play().catch(e => {
                console.error("Lỗi phát video:", e);
                onStatus("LỖI: KHÔNG THỂ PHÁT VIDEO");
              });
            };
            onStatus("AI SẴN SÀNG: HIỂN THỊ BÀN TAY");
            predictWebcam();
          }
        } else {
            onStatus("LỖI: CAMERA BỊ TỪ CHỐI QUYỀN TRUY CẬP");
        }
      } catch (err: any) {
        console.error("Lỗi setup:", err);
        onStatus(`LỖI: ${err.message || 'MÔ HÌNH TẢI THẤT BẠI'}`);
      }
    };

    const predictWebcam = () => {
      if (gestureRecognizer && videoRef.current && canvasRef.current) {
        if (videoRef.current.videoWidth > 0 && videoRef.current.videoHeight > 0) {
          try {
            const results = gestureRecognizer.recognizeForVideo(videoRef.current, Date.now());
            const ctx = canvasRef.current.getContext("2d");
            
            if (ctx && debugMode) {
                ctx.clearRect(0, 0, canvasRef.current.width, canvasRef.current.height);
                canvasRef.current.width = videoRef.current.videoWidth; 
                canvasRef.current.height = videoRef.current.videoHeight;
                if (results.landmarks && results.landmarks.length > 0) {
                  for (const landmarks of results.landmarks) {
                    const drawingUtils = new DrawingUtils(ctx);
                    drawingUtils.drawConnectors(landmarks, GestureRecognizer.HAND_CONNECTIONS, { color: "#FFD700", lineWidth: 2 });
                    drawingUtils.drawLandmarks(landmarks, { color: "#FF0000", lineWidth: 1 });
                  }
                }
            } else if (ctx && !debugMode) {
              ctx.clearRect(0, 0, canvasRef.current.width, canvasRef.current.height);
            }

            if (results.gestures && results.gestures.length > 0) {
              const name = results.gestures[0][0].categoryName; 
              const score = results.gestures[0][0].score;
              
              if (score > 0.4) {
                 if (name === "Open_Palm") onGesture("CHAOS"); 
                 if (name === "Closed_Fist") onGesture("FORMED");
                 if (debugMode) onStatus(`PHÁT HIỆN: ${name} (${(score*100).toFixed(0)}%)`);
              }
              
              if (results.landmarks && results.landmarks.length > 0) {
                const speed = (0.5 - results.landmarks[0][0].x) * 0.15;
                onMove(Math.abs(speed) > 0.01 ? speed : 0);
              }
            } else { 
              onMove(0); 
              if (debugMode) onStatus("AI SẴN SÀNG: KHÔNG CÓ BÀN TAY"); 
            }

            // Phát hiện cử chỉ pinch (chụm ngón tay)
            if (results.landmarks && results.landmarks.length > 0 && Date.now() - pinchCooldownRef.current > 500) {
              const landmarks = results.landmarks[0];
              if (landmarks && landmarks.length >= 9) {
                const thumbTip = landmarks[4]; // Ngón cái
                const indexTip = landmarks[8]; // Ngón trỏ
                
                const distance = Math.sqrt(
                  Math.pow(thumbTip.x - indexTip.x, 2) + Math.pow(thumbTip.y - indexTip.y, 2)
                );
                
                // Nếu khoảng cách nhỏ (< 0.05), phát hiện pinch
                if (distance < 0.05) {
                  onPinch();
                  pinchCooldownRef.current = Date.now();
                  if (debugMode) onStatus("CỬ CHỈ CHỌN ẢNH: PINCH DETECTED");
                }
              }
            }
          } catch (err) {
            console.error("Lỗi nhận dạng:", err);
            if (debugMode) onStatus("LỖI: Nhận dạng thất bại");
          }
        }
        requestRef = requestAnimationFrame(predictWebcam);
      }
    };
    setup();

    return () => {
      cancelAnimationFrame(requestRef);
      if (videoRef.current && videoRef.current.srcObject) {
        const stream = videoRef.current.srcObject as MediaStream;
        stream.getTracks().forEach(track => track.stop());
      }
    };
  }, [enabled, onGesture, onMove, onStatus, debugMode, onPinch]);

  return (
    <>
      <video ref={videoRef} style={{ opacity: debugMode ? 0.6 : 0, position: 'fixed', top: 0, right: 0, width: debugMode ? '320px' : '1px', zIndex: debugMode ? 100 : -1, pointerEvents: 'none', transform: 'scaleX(-1)' }} playsInline muted autoPlay />
      <canvas ref={canvasRef} style={{ position: 'fixed', top: 0, right: 0, width: debugMode ? '320px' : '1px', height: debugMode ? 'auto' : '1px', zIndex: debugMode ? 101 : -1, pointerEvents: 'none', transform: 'scaleX(-1)' }} />
    </>
  );
};

